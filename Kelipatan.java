public class Kelipatan {
    public static void main(String[] args) {
        System.out.println("LATIHAN 18. KELIPATAN");

        // Looping from 3.5 to 35 in multiples of 3.5
        for (double i = 3.5; i <= 35; i += 3.5) {
            System.out.println(i);
        }
    }
}

